package com.callor.hello;

public class Numbers {

	public static void main(String[] args) {
		
		// 4칙연산과 나머지연산
		System.out.println("33 + 2 = " + (33 + 2));
		System.out.println("33 - 2 = " + (33 - 2));
		System.out.println("33 * 2 = " + (33 * 2));
		System.out.println("33 / 2 = " + (33 / 2));
		System.out.println("33 % 2 = " + (33 % 2));
		
		
	}

}
